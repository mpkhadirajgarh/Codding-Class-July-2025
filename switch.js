// 1 = Monday , 2 = Tuesday, 3 = Wednessday , 

let DayNumber = -1 ; 

switch (DayNumber) {
    case 1 : 
    console.log("Monday");
    break;
    case 2 : 
    console.log('Tuesday');
    break;
    case 3 : 
    console.log('Wednessday');
    break;
    case 4 : 
    console.log('Thursday');
    break;
    case 5 : 
    console.log('Friday');
    break;
    case 6 : 
    console.log('Saturday');
    break;
    case 7 : 
    console.log('Sunday is Last day of WEEK');
    break;
    default: console.log('You are entered wrong Number');

}
